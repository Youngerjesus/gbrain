#!/usr/bin/env bash
set -euo pipefail

EVAL_ID="${1:-promptfoo-eval}"
ROOT="${2:-.}"
SKILL_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

SAFE_ID="$(printf '%s' "$EVAL_ID" | tr -c 'A-Za-z0-9._-' '-')"
TARGET_DIR="$ROOT/.runtime/promptfoo-eval/$SAFE_ID"

mkdir -p "$TARGET_DIR/providers"
cp "$SKILL_DIR/assets/templates/ephemeral-promptfooconfig.yaml" "$TARGET_DIR/promptfooconfig.yaml"
cp "$SKILL_DIR/assets/templates/read_file_provider.js" "$TARGET_DIR/providers/read_file_provider.js"
chmod +x "$TARGET_DIR/providers/read_file_provider.js"

cat > "$TARGET_DIR/README.md" <<README
# Ephemeral Promptfoo Eval Harness

This harness was generated by the promptfoo-eval skill.

- Edit \`promptfooconfig.yaml\` with project-specific tests.
- Run with:

\`\`\`bash
~/.codex/skills/promptfoo-eval/scripts/run_promptfoo_eval.sh "$TARGET_DIR/promptfooconfig.yaml" .runtime/promptfoo "$SAFE_ID"
\`\`\`

Keep this directory local unless the user explicitly asks to commit reusable eval coverage.
README

printf '%s\n' "$TARGET_DIR"
